/**
 *  border-radius:  圆角边框
 *                  1. border-radius:10px;              //所有角都使用半径为10px的圆角
 *                  2. border-radius: 5px 4px 3px 2px;  //顺时针, 四个半径值分别是左上角、右上角、右下角和左下角
 *
 *                  分量写法： border-top-left-radius:  左上角
 *                            border-top-right-radius: 右上角
 *                            border-bottom-left-radius:  左下角
 *                            border-bottom-right-radius: 右下角
 */

/**
 *  box-shadow:  边框 阴影
 *               box-shadow: X轴偏移量 Y轴偏移量 [阴影模糊半径] [阴影扩展半径] [阴影颜色] [投影方式];
 *               可以添加一个或者多个阴影
 *               1. box-shadow: 4px 2px 4px #333333;       外阴影
 *               2. box-shadow: 4px 2px 4px #333333 inset; 内阴影
 *
 *               阴影模糊半径   和    阴影扩展半径 区别：
 */

